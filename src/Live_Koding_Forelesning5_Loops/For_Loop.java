package Live_Koding_Forelesning5_Loops;

public class For_Loop {
    public static void main(String[]args){

        for (int i = 3; i>0; i--){
            System.out.println("Itiretaion nr: " + i);

        }

        System.out.println("Loop Completed!!");

        for(int i = 0; i < 3; i++){
            for(int j = 0; j < 4; j++){
                System.out.println("YEEYeee");
            }
        }
    }
}
